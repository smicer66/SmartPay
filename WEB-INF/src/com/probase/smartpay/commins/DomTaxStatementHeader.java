package com.probase.smartpay.commins;

public class DomTaxStatementHeader {
	private String acctNameHdr;
	private String acctNoHdr;
	private String bankCodeHdr;
	private String closingBalHdr;
	private String openingBalHdr;
	private String stmtDateHdr;
	private String stmtNoHdr;
	public String getAcctNameHdr() {
		return acctNameHdr;
	}
	public void setAcctNameHdr(String acctNameHdr) {
		this.acctNameHdr = acctNameHdr;
	}
	public String getAcctNoHdr() {
		return acctNoHdr;
	}
	public void setAcctNoHdr(String acctNoHdr) {
		this.acctNoHdr = acctNoHdr;
	}
	public String getBankCodeHdr() {
		return bankCodeHdr;
	}
	public void setBankCodeHdr(String bankCodeHdr) {
		this.bankCodeHdr = bankCodeHdr;
	}
	public String getClosingBalHdr() {
		return closingBalHdr;
	}
	public void setClosingBalHdr(String closingBalHdr) {
		this.closingBalHdr = closingBalHdr;
	}
	public String getOpeningBalHdr() {
		return openingBalHdr;
	}
	public void setOpeningBalHdr(String openingBalHdr) {
		this.openingBalHdr = openingBalHdr;
	}
	public String getStmtDateHdr() {
		return stmtDateHdr;
	}
	public void setStmtDateHdr(String stmtDateHdr) {
		this.stmtDateHdr = stmtDateHdr;
	}
	public String getStmtNoHdr() {
		return stmtNoHdr;
	}
	public void setStmtNoHdr(String stmtNoHdr) {
		this.stmtNoHdr = stmtNoHdr;
	}
}
