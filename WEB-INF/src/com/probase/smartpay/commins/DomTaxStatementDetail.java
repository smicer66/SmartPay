package com.probase.smartpay.commins;

public class DomTaxStatementDetail {
	private String balanceRow;
	private String creditRow;
	private String debitRow;
	private String descriptionRow;
	private String prnRow;
	private String tpinRow;
	private String transactionCodeRow;
	private String transactionDateRow;
	public String getBalanceRow() {
		return balanceRow;
	}
	public void setBalanceRow(String balanceRow) {
		this.balanceRow = balanceRow;
	}
	public String getCreditRow() {
		return creditRow;
	}
	public void setCreditRow(String creditRow) {
		this.creditRow = creditRow;
	}
	public String getDebitRow() {
		return debitRow;
	}
	public void setDebitRow(String debitRow) {
		this.debitRow = debitRow;
	}
	public String getDescriptionRow() {
		return descriptionRow;
	}
	public void setDescriptionRow(String descriptionRow) {
		this.descriptionRow = descriptionRow;
	}
	public String getPrnRow() {
		return prnRow;
	}
	public void setPrnRow(String prnRow) {
		this.prnRow = prnRow;
	}
	public String getTpinRow() {
		return tpinRow;
	}
	public void setTpinRow(String tpinRow) {
		this.tpinRow = tpinRow;
	}
	public String getTransactionCodeRow() {
		return transactionCodeRow;
	}
	public void setTransactionCodeRow(String transactionCodeRow) {
		this.transactionCodeRow = transactionCodeRow;
	}
	public String getTransactionDateRow() {
		return transactionDateRow;
	}
	public void setTransactionDateRow(String transactionDateRow) {
		this.transactionDateRow = transactionDateRow;
	}
	
}
