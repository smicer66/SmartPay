package com.probase.smartpay.commins;

public class MiscTransactionToBePaid {

	private String transactionCode;
	private String companyCode;
	private String referenceOffice;
	private String referenceYear;
	private String referenceSerial;
	private String referenceNumber;
	private String referenceText;
	private String amountToBePaid;
	public String getTransactionCode() {
		return transactionCode;
	}
	public void setTransactionCode(String transactionCode) {
		this.transactionCode = transactionCode;
	}
	public String getCompanyCode() {
		return companyCode;
	}
	public void setCompanyCode(String companyCode) {
		this.companyCode = companyCode;
	}
	public String getReferenceOffice() {
		return referenceOffice;
	}
	public void setReferenceOffice(String referenceOffice) {
		this.referenceOffice = referenceOffice;
	}
	public String getReferenceYear() {
		return referenceYear;
	}
	public void setReferenceYear(String referenceYear) {
		this.referenceYear = referenceYear;
	}
	public String getReferenceSerial() {
		return referenceSerial;
	}
	public void setReferenceSerial(String referenceSerial) {
		this.referenceSerial = referenceSerial;
	}
	public String getReferenceNumber() {
		return referenceNumber;
	}
	public void setReferenceNumber(String referenceNumber) {
		this.referenceNumber = referenceNumber;
	}
	public String getReferenceText() {
		return referenceText;
	}
	public void setReferenceText(String referenceText) {
		this.referenceText = referenceText;
	}
	public String getAmountToBePaid() {
		return amountToBePaid;
	}
	public void setAmountToBePaid(String amountToBePaid) {
		this.amountToBePaid = amountToBePaid;
	}
}
